// For loop that prints 1-20
for(let i = 1; i <= 20; i++) {
    console.log(i)
};

// For loop that logs 1-20 tripled
for(let i = 1; i <= 20; i++) {
    console.log(i * 3)
};

for(let i = 1; i <= 20; i++) {
    if(i % 2 !== 0) {
        console.log("odd")
    } else {
        console.log(i);
    }
}
